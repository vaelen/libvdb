/* CRC-32/CKSUM Implementation */

uint32 CRC32(const void *data, int16 len) {
    uint32 crc, total_len;
    CRC32Start(&crc, &total_len, data, len);
    return CRC32End(&crc, total_len);
}

uint32 CRC32Start(uint32 *crc, uint32 *total_len, const void *data, int16 len) {
    *crc = 0x00000000UL;
    *total_len = 0;
    return CRC32Add(crc, total_len, data, len);
}

uint32 CRC32Add(uint32 *crc, uint32 *total_len, const void *data, int16 len) {
    const byte *p = (const byte *)data;
    int16 i;
    for (i = 0; i < len; i++) {
        byte idx = (byte)((*crc >> 24) ^ p[i]);
        *crc = (*crc << 8) ^ crc32_table[idx];
    }
    *total_len += (uint32)len;
    return *crc;
}

uint32 CRC32End(uint32 *crc, uint32 total_len) {
    int i;
    for (i = 0; i < 4; i++) {
        *crc = (*crc << 8) ^ crc32_table[(byte)(*crc >> 24) ^ (byte)(total_len >> (8 * (3 - i)))];
    }
    return ~(*crc);
}

/* SHA-1 Implementation */

static uint32 rotateLeft(uint32 value, int bits) {
    return (value << bits) | (value >> (32 - bits));
}

static void sha1ProcessBlock(SHA1Context *ctx) {
    uint32 w[80];
    uint32 a, b, c, d, e, f, k, temp;
    int t;

    /* Prepare message schedule */
    for (t = 0; t < 16; t++) {
        w[t] = ((uint32)ctx->buffer[t * 4] << 24) |
               ((uint32)ctx->buffer[t * 4 + 1] << 16) |
               ((uint32)ctx->buffer[t * 4 + 2] << 8) |
               (uint32)ctx->buffer[t * 4 + 3];
    }
    for (t = 16; t < 80; t++) {
        w[t] = rotateLeft(w[t-3] ^ w[t-8] ^ w[t-14] ^ w[t-16], 1);
    }

    a = ctx->h0; b = ctx->h1; c = ctx->h2; d = ctx->h3; e = ctx->h4;

    for (t = 0; t < 80; t++) {
        if (t < 20) {
            f = (b & c) | ((~b) & d);
            k = 0x5A827999UL;
        } else if (t < 40) {
            f = b ^ c ^ d;
            k = 0x6ED9EBA1UL;
        } else if (t < 60) {
            f = (b & c) | (b & d) | (c & d);
            k = 0x8F1BBCDCUL;
        } else {
            f = b ^ c ^ d;
            k = 0xCA62C1D6UL;
        }
        temp = rotateLeft(a, 5) + f + e + k + w[t];
        e = d; d = c; c = rotateLeft(b, 30); b = a; a = temp;
    }

    ctx->h0 += a; ctx->h1 += b; ctx->h2 += c; ctx->h3 += d; ctx->h4 += e;
}

void SHA1Start(SHA1Context *ctx) {
    ctx->h0 = 0x67452301UL;
    ctx->h1 = 0xEFCDAB89UL;
    ctx->h2 = 0x98BADCFEUL;
    ctx->h3 = 0x10325476UL;
    ctx->h4 = 0xC3D2E1F0UL;
    ctx->msg_len_lo = 0;
    ctx->msg_len_hi = 0;
    ctx->buffer_len = 0;
}

void SHA1Add(SHA1Context *ctx, const void *data, int16 len) {
    const byte *p = (const byte *)data;
    int16 i;
    uint32 bit_add;

    for (i = 0; i < len; i++) {
        ctx->buffer[ctx->buffer_len] = p[i];
        ctx->buffer_len++;
        if (ctx->buffer_len == 64) {
            sha1ProcessBlock(ctx);
            ctx->buffer_len = 0;
        }
    }

    /* Update 64-bit message length in bits using lo/hi */
    bit_add = (uint32)len * 8;
    ctx->msg_len_lo += bit_add;
    if (ctx->msg_len_lo < bit_add)
        ctx->msg_len_hi++;
}

void SHA1End(SHA1Context *ctx, byte digest[SHA1_DIGEST_SIZE]) {
    int i;

    /* Append padding bit */
    ctx->buffer[ctx->buffer_len] = 0x80;
    ctx->buffer_len++;

    /* If not enough room for length, pad and process */
    if (ctx->buffer_len > 56) {
        while (ctx->buffer_len < 64) {
            ctx->buffer[ctx->buffer_len] = 0;
            ctx->buffer_len++;
        }
        sha1ProcessBlock(ctx);
        ctx->buffer_len = 0;
    }

    /* Pad with zeros */
    while (ctx->buffer_len < 56) {
        ctx->buffer[ctx->buffer_len] = 0;
        ctx->buffer_len++;
    }

    /* Append length in bits as 64-bit big-endian */
    ctx->buffer[56] = (byte)(ctx->msg_len_hi >> 24);
    ctx->buffer[57] = (byte)(ctx->msg_len_hi >> 16);
    ctx->buffer[58] = (byte)(ctx->msg_len_hi >> 8);
    ctx->buffer[59] = (byte)(ctx->msg_len_hi);
    ctx->buffer[60] = (byte)(ctx->msg_len_lo >> 24);
    ctx->buffer[61] = (byte)(ctx->msg_len_lo >> 16);
    ctx->buffer[62] = (byte)(ctx->msg_len_lo >> 8);
    ctx->buffer[63] = (byte)(ctx->msg_len_lo);

    sha1ProcessBlock(ctx);

    /* Produce final hash value (big-endian) */
    for (i = 0; i < 4; i++) {
        digest[i]      = (byte)(ctx->h0 >> (24 - i * 8));
        digest[i + 4]  = (byte)(ctx->h1 >> (24 - i * 8));
        digest[i + 8]  = (byte)(ctx->h2 >> (24 - i * 8));
        digest[i + 12] = (byte)(ctx->h3 >> (24 - i * 8));
        digest[i + 16] = (byte)(ctx->h4 >> (24 - i * 8));
    }
}

void SHA1(const void *data, int16 len, byte digest[SHA1_DIGEST_SIZE]) {
    SHA1Context ctx;
    SHA1Start(&ctx);
    SHA1Add(&ctx, data, len);
    SHA1End(&ctx, digest);
}
