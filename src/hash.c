/* CRC-16/KERMIT Implementation */

uint16 CRC16(const void *data, int16 len) {
    uint16 crc;
    CRC16Start(&crc, data, len);
    return CRC16End(&crc);
}

uint16 CRC16Start(uint16 *crc, const void *data, int16 len) {
    *crc = 0x0000;
    return CRC16Add(crc, data, len);
}

uint16 CRC16Add(uint16 *crc, const void *data, int16 len) {
    const byte *p = (const byte *)data;
    int16 i;
    for (i = 0; i < len; i++) {
        byte idx = (byte)(*crc) ^ p[i];
        *crc = (*crc >> 8) ^ crc16_kermit_table[idx];
    }
    return *crc;
}

uint16 CRC16End(uint16 *crc) {
    return *crc;
}

